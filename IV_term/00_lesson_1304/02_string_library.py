import string
'''string - отдельный модуль в Python для работы
со строчными данными'''
print(string.ascii_letters)  # все буквы английского алфавита
print(string.ascii_lowercase)  # все маленькие буквы английского алфавита
print(string.ascii_uppercase)  # все большие буквы английского алфавита
print(string.digits)  # все числа от 0 до 9 В ВИДЕ СТРОКИ
print(string.hexdigits)  # все числа 16-ричной системы
print(string.octdigits)  # все числа 8-ричной системы
print(string.punctuation)  # все знаки препинания
print(string.printable)  # возвращает все значения
print(string.whitespace)  # пробел




from random import randint
# функция randint(a, b) возвращает случайные ЦЕЛЫЕ числа
# в промежутке от a до b

r_num = randint(-1000, 1000)
print(f'Число: {r_num}.')
print(f'Четность числа: {r_num % 2 == 0}.')
print(f'Нечетность числа: {r_num % 2 != 0}.')
# проверяю, что остаток от деления r_num на 2,
# равен нулю/число четное
